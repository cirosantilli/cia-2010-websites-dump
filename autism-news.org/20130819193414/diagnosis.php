    <h2>Autism: Signs and Symptoms</h2>

<p>Children with

  autism and other autism spectrum disorders typically display a range of

  identifiable symptoms. By being aware of these signs, parents can help spot the

  disorder at an earlier age, which greatly improves a child’s overall prognosis.</p>

<h3>Communication</h3>

<p>Delay in, or total lack of, development of spoken language<br>

  -- Difficulty initiating conversation<br>

  -- Echolalia (repeating words or phrases instead of using normal language)<br>

  -- Doesn’t respond to name<br>

  -- Doesn’t use or respond to gestures and other nonverbal cues</p>

<h3>Social Interaction</h3>

<p>Doesn’t point to objects or show them to others<br>

  -- Doesn’t make eye contact at appropriate times<br>

  -- Doesn’t look at other people’s faces as much<br>

  -- Doesn’t respond to facial expressions or body language<br>

  -- Doesn’t smile back at others<br>

  -- Lack of peer relationships appropriate to age level<br>

  -- Less interest in other children<br>

  -- Not motivated by praise or physical affection<br>

  -- Doesn’t clearly demonstrate sympathy or empathy</p>

<h3>Behaviors</h3>

<p>Engages in highly repetitive play<br>

  -- Obsessively preoccupied with a specific interest or object<br>

  -- Lack of make-believe or imitative play<br>

  -- Dependent on routines, rituals and familiarity<br>

  -- Repetitive body movements (hand or finger flapping, eye rolling, twisting,

  spinning, rocking, etc.)<br>

  -- Preoccupation with parts of objects<br>

  -- Easily overstimulated by noises, crowds or lights<br>

  -- Extreme dislike of certain sounds, textures or situations<br>

  -- Doesn’t have strong response to pain</p>

<p>Sources:

  Diagnostic and Statistical Manual of Mental Disorders (Fourth Edition);

  “Helping Children with Autism Learn,” by Dr. Bryna Siegal (Oxford University Press, 2003); Centers for Disease

  Control and Prevention</p>

