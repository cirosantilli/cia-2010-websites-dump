<h3>Increase in diagnoses of autism</h3><p class="info">Source: Wikipedia.org</p>

<p class="right"><img src="images/family-taking-walk.jpg" alt="family walk" />The number of reported cases of autism has increased dramatically over the past decade.There has been an explosion 

worldwide in reported cases of autism over the last ten years. In the last decade, the population of the United States 

has increased by 13%. There has been an increase in non-autism-related disabilities of 16%. The increase in autism is 173%.

There has been considerable speculation as to why this might be, with no conclusive proof emerging around any theory. 

Epidemiologists argue that the rise is either partly or entirely attributable to changes in diagnostic criteria, 

reclassifications, public awareness, and the incentive to tap into federally mandated services.</p>

 

<p>A widely cited study from the M.I.N.D. Institute in California (Oct 17, 2002), concluded that the increase is real, 

even after those complicating factors are accounted for. Other researchers remain unconvinced, including 

Dr. Chris Johnson, a professor of pediatrics at the University of Texas Health Sciences Center at San Antonio 

and co-chair of the American Academy of Pediatrics Autism Expert Panel, who says, 

“There is a chance we’re seeing a true rise, but right now I don’t think anybody can answer that question for sure.” 

(Newsweek reference below).</p>



<p>The answer to this question has significant ramifications on the direction of research, since a real increase 

would focus more attention (and research funding) on the search for environmental factors, while a consensus 

for little or no real increase would direct more attention to genetics. On the other hand, it is conceivable 

that certain environmental factors (vaccination, diet, societal changes) may have a particular impact on people 

with a specific genetic constitution.</p>

 

<p>There is little public research on the effects of in-vitro fertilization on the number of incidences of autism.</p>

