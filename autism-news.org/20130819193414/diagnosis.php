		<h2>What are some common signs of autism?</h2>

		<p>There are three distinctive behaviors that characterize autism.    Autistic children have difficulties 

		with social interaction, problems with verbal and nonverbal communication, and repetitive behaviors or narrow, 

		obsessive interests.  These behaviors can range in impact from mild to disabling.</p>

		<p>The hallmark feature of autism is impaired social interaction.  Parents are usually the first to notice 

		symptoms of autism in their child.  As early as infancy, a baby with autism may be unresponsive to people 

		or focus intently on one item to the exclusion of others for long periods of time.  A child with autism 

		may appear to develop normally and then withdraw and become indifferent to social engagement.</p>

		<p>Children with autism may fail to respond to their name and often avoid eye contact with other people. 

		They have difficulty interpreting what others are thinking or feeling because they can’t understand social cues, 

		such as tone of voice or facial expressions, and don’t watch other people’s faces for clues about 

		appropriate behavior.  They lack empathy.</p>

		<p>Many children with autism engage in repetitive movements such as rocking and twirling, 

		or in self-abusive behavior such as biting or head-banging.  They also tend to start speaking 

		later than other children and may refer to themselves by name instead of “I” or “me.”  

		Children with autism don’t know how to play interactively with other children.  Some speak 

		in a sing-song voice about a narrow range of favorite topics, with little regard for the 

		interests of the person to whom they are speaking.</p>

		<p>Many children with autism have a reduced sensitivity to pain, but are abnormally sensitive 

		to sound, touch, or other sensory stimulation.  These unusual reactions may contribute to 

		behavioral symptoms such as a resistance to being cuddled or hugged.</p>

		<p>Children with autism appear to have a higher than normal risk for certain co-existing conditions, 

		including fragile X syndrome (which causes mental retardation), tuberous sclerosis (in which tumors 

		grow on the brain), epileptic seizures, Tourette syndrome, learning disabilities, and attention 

		deficit disorder.  For reasons that are still unclear, about 20 to 30 percent of children with 

		autism develop epilepsy by the time they reach adulthood.  While people with schizophrenia 

		may show some autistic-like behavior, their symptoms usually do not appear until the late 

		teens or early adulthood.  Most people with schizophrenia also have hallucinations and 

		delusions, which are not found in autism.</p>

