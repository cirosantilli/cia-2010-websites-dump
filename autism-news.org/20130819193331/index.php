        <h2>What is Autism?</h2>

        <p>Autism is a developmental disorder that is characterized by impaired development in communication, social interaction, and behavior. Autism is classified as a Pervasive Developmental Disorder (PDD), which is part of a broad spectrum of developmental disorders affecting young children and adults--the Autistic Spectrum Disorders (ASD). The range of these disorders varies from severely impaired individuals with autism to other individuals who have abnormalities of social interaction but normal intelligence--Asperger's syndrome. The ways in which autism is exhibited can differ greatly. Additionally, autism can be found in association with other disorders such as mental retardation and certain medical conditions. The degree of autism can range from mild to severe. Mildly affected individuals may appear very close to normal. Severely afflicted individuals may be extremely retarded and unable to function in almost any setting.</p>

        <p>In the past, autism has been confused with childhood schizophrenia or childhood psychosis. As additional research information about autism becomes available, the scope and definition of the condition continues to become more refined. Much of the past confusion about the disorder has been resolved.</p>

