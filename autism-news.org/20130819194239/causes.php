		<h2>Causes of autism theories</h2>

		<p class="info">Source: Wikipedia.org</p>

<h3>Brain testosterone theory</h3>

<p>Simon Baron-Cohen proposes a model for autism based in his

 <a href="http://www.guardian.co.uk/life/feature/story/0,13026,937913,00.html" target="new">empathising-systemising (E-S) theory</a>.

 His team at the Autism Research Centre in Cambridge, UK, measured testosterone levels in the amniotic fluid of mothers

 while pregnant. This is presumed to reflect levels in the babies themselves. The team found that the babies with higher

 fetal testosterone levels had a smaller vocabulary and made eye contact less often when they were a year old.

His group has looked at the original 58 children again, at age four. The researchers found that the children with higher

testosterone in the womb are less developed socially, and the interests of boys are more restricted than girls.

The results will be published in the Journal of Child Psychology and Psychiatry (2004).

Baron-Cohen theorizes that high fetal testosterone levels push brain development towards an improved ability to see

patterns and analyse systems. Males supposedly tend to be better at these tasks than females. But the high levels

are thought to inhibit the development of communication and empathy, which are allegedly typical female skills.

(New Scientist, 24 May 2003). There is still no demonstrable evidence that testosterone levels affect brain

development at all, let alone autism. Gender or bio-determinism is a fashionable explanation for many human

behaviours, but has been challenged by other professionals</a>.</p>



<h3>Vaccine theory</h3>

<p class="right"><img src="images/vaccine.jpg" alt="vaccine photo" width="108" height="89" />Research by Andrew Wakefield in the UK, published in The Lancet in February 1998 suggested a possible link between autism and the MMR vaccine. This was very controversial. Subsequent studies failed to confirm the link, and some in fact showed a lack of such a link. <a href="http://briandeer.com/mmr-lancet.htm" target="new">The original research has come under criticism</a>, largely due to a conflict of interest on Wakefield's part. In February 2004 The Lancet described the research as "entirely flawed" and said that it should never have been published. Controversy continues, with Wakefield defending his integrity.

Critics with statistical skills have claimed that Wakefield is incompetent in statistics, and failed to recognize the bias of his sample, among other obvious flaws. Wakefield's nonscientific study and its continuing circulation in autism societies is sometimes depicted by the media as a "proof" no autism-vaccine connection exists and "it's 100% hype", a position that wasn't proven either (even if most cases are not MMR or vaccine related).

Research in the US suggesting a similar link between autism and DPT vaccine. It isn't however the large majority of autism that would come from vaccines, unlike early claims from Wakefield.</p>



<h3>Brain trauma</h3>

<p>Susan Bryson has said that some autistics have evidence of trauma to the brain stem in early development, and that a small portion of the thalidomide victims have become autistic. The victims' limbs were normal unless thalidomide use continued later in the pregnancy. The brain stem anomaly's most striking feature is inability to focus attention away from a stimulus in a short time like neurotypicals, as demonstrated in a psychological test.

Neurology-skilled Aspies claim the inability to shift attention quickly interferes with the ability to read nonverbal language where fast attention shifts are needed (such as eye language), suggesting that being nonverbal is not a primary feature of autism. Strong and shiftless focus is however a benefit in some areas like science, programming, and advanced mathematics. This is supported by the monotropism hypothesis.

Dr. Bernard Rimland's influential research and his book Infantile Autism (1967) argued that autism was not caused by childhood trauma or abuse, but by damage to certain areas of the brain, particularly the reticular formation which associates present sensory input with memories of past experiences. Dr. Rimland is a foremost advocate of the theory that <a href="http://www.curezone.com/art/read.asp?ID=79&amp;db=2&amp;C0=735" target="new">autism may be precipitated by mercury and heavy metal toxicity</a>. He also is prominent in increasingly common claims of successful treatment of autism in children—particularly regarding improvements in ability to comprehend the spoken word—with the gluten-free, casein-free diet and mercury chelation therapy.

Neurobiology-skilled Aspies have often claimed Dr. Bernard Rimland's methods cure heavy metal poisoning, but not autism. Curing heavy metal poisoning when it is present is a worthy goal (it helps with IQ and other learning difficulties as well as general health), but claiming a cure for autism is a misrepresentation. Heavy metal poisoning may be more common among autistics due to a severe metallothionein deficiency, but heavy metals don't cause autism. They might make you more likely to get an autism diagnosis, though.</p>



<h3>Viruses or bacterial infection</h3>

<p>A growing body of peer-reviewed studies published in mainstream journals has shown that many common diseases

of unknown origin are in fact caused by the presence of slowly acting viruses. For example, cervical cancer is

caused by the human papilloma wart virus; some cases of liver cancer are caused by hepatitis C or B;

Schizophrenia may be caused by Borna virus. Paul W. Ewald, among others, argues that the available data

on the origin of autism is consistent with it being caused by a virus or bacterial infection.</p>

