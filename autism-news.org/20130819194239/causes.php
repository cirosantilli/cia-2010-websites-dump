		<h2>Roots of autism more complex than thought</h2>

		<p class="info">February 19, 2007<br />By Liz Szabo (USA Today)</p>

<p>A preliminary new study sheds light on the possible genetic roots of autism.</p>

<p>Experts say autism, a brain disorder that inhibits the ability to communicate and develop

  relationships, appears even more complex than doctors imagined.</p>

<p>The study, published Sunday in <i>Nature Genetics</i>, was led by the Autism Genome Project, a

  collaboration among researchers in 19 countries. The project was financed by

  the National Institutes of Health and the advocacy group Autism Speaks.</p>

<p>Scientists have long known

  that autism may run in families. For example, if one identical twin has autism,

  the other twin is much more likely to have the disorder. And doctors also have

  noticed that autism can cause a wide range of disabilities that afflict some

  children far more severely than others, says Andy Shih, chief science officer

  for Autism Speaks. </p>

<p>The new research suggests there could be many genes that, in combination with environmental influences,

  predispose children to autism, Shih says. That would be very different from

  other disorders, such as cystic fibrosis, which are caused by a single gene. </p>

<p>&quot;We expect genetic studies will show that we're not just talking about one disease,&quot; he says.

  &quot;It could be autism<i>s.</i>&quot; </p>

<p>In the study, researchers

  compared genetic samples from nearly 1,200 families in which two or more

  siblings have autism or related conditions. </p>

<p>With a larger DNA sample,

  Shih says, scientists can be more confident in their findings. Past studies

  included DNA from only 200 or 300 families.</p>

<p>In the new study, scientists looked for telltale patterns — genes that turned up repeatedly,

  which could indicate that they play a role in autism, Shih says.</p>

<p>Scientists found suspicious patterns in several spots on the genome, Shih says. But doctors are

  most intrigued by two possible candidates: a previously unidentified section of

  chromosome 11, which is home to many genes; and one particular gene, located

  elsewhere, called neurexin-1, which appears to be involved in communication

  among brain cells.</p>

<p>Scientists also looked for

  &quot;copy number variations,&quot; places where DNA has been either inserted

  or deleted. Although a small number of patients had these variations,

  relatively few were exactly the same, Shih says. And though some patients

  shared these insertions and deletions with their parents, others did not. </p>

<p>Stan Nelson, a genetics professor at the University of California-Los Angeles,

says researchers eventually may use these findings to develop tools to

  better diagnose and understand the disease. Some genes may prove more

  influential than others. </p>

<p>Nelson, a study co-author, says some genes may contribute to 20% of cases, and others cause only 0.2%.</p>

<p>But Rita Cantor, a co-author also at UCLA, notes that such discoveries will come slowly.</p>

<p>&quot;We'll have to search very hard to find the genes,&quot; Cantor says. &quot;That's not

  five minutes. That's a lot of work.&quot;</p>

