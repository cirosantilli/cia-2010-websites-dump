<h2>Proposed models of autism</h2>



<p>A genome screen of autism. Most chromosomes seem related to autism, in particular chromosomes 2, 7 and 15.

The causes and origins of autism and Asperger's syndrome are a source of continuing conjecture and debate.

Amongst several competing theories are the <a href="http://www.cmu.edu/PR/releases04/040727_autism.html" target="new">underconnectivity theory</a>

developed by cognitive scientists at Carnegie Mellon University and the University of Pittsburgh, the <a href="http://www.rdos.net/eng/asperger.htm" target="new">Neanderthal theory</a>,

the extreme male brain theory by Simon Baron Cohen, the lack of theory of mind, and the Preoperational-autism theory.</p>





<h3>The extreme male brain theory</h3>

<p>The extreme male brain theory of autism, proposed by Simon Baron-Cohen in accordance with

his <a href="http://www.guardian.co.uk/print/0,3858,4649492-111414,00.html" target="new">empathizing-systemizing theory</a>,

suggests that autistics have extreme forms of what he describes as the male brain: they're good (often very good) at systemising, and very bad at empathizing.

The poetry composed by autistics who can communicate by <a href="http://archive.autistics.org/library/tito-can.html" target="new">writing</a> would seem to challenge this idea, but the scientific jury is still out.</p>





<h3>The Preoperational-Autism Theory</h3>

<p>The Preoperational-Autism theory states that autistic people are those who get neurologically stuck

at the pre-operational stage of cognitive development, where much of information processing is at a

wholistic-visual level and largely musical and non-verbal. This also addresses the issue of the

theory of mind where children at the pre-operational stage of cognitive development have not

attained decentralization from egocentrism.</p>





<h3>The Neanderthal Theory</h3>

<p>The Neanderthal Theory is that autism and other psychiatric conditions evolved from interbreeding

between Homo Sapiens and Neanderthals. This interbreeding is believed to have caused genetic material

from the Neanderthals to enter the Homo Sapiens genome. Whether such interbreeding ever actually

occurred is controversial, and this theory is extremely speculative.</p>





<h3>Monotropism</h3>

<p>The monotropism hypothesis argues that the central feature of autism is attention-tunneling, or monotropism.

The hypothesis is founded on the model <a href="http://www.autismandcomputing.org.uk/mind.htm" target="new">Mind as a Dynamical System: Implications for Autism</a>.

In this model of mind, the fundamental and limited resource is mental attention.

Mental events compete for and consume attention. In a polytropic mind, many interests are

aroused to a moderate degree. In a monotropic mind, few interests are very highly aroused.

When many interests are aroused, multiple complex behaviours emerge. When few interests are aroused,

a few intensely motivated behaviours are engendered. From monotropism hypothesis, autism results from

different strategies of distributing attention in the brain.</p>





<h3>Underconnectivity Theory</h3>

<p>The underconnectivity theory indicates a deficiency in the coordination among brain areas

(the brain is known to be modular). With the aid of (fMRI), it was seen that white matter,

which connects various areas of the brain like cables, has abnormalities in people with autism.</p>



<p>The underconnectivity theory holds that autism is a system-wide brain disorder that limits the coordination

and integration among brain areas. This theory is parsimonious, in that it explains why autistic people are matured

on certain dimensions eg: visual information processing and logical analysis, and yet are socially and sometimes

neuro-physiologically, significantly younger than their chronological age. The underconnectivity theory can be

regarded as monotropism in the brain.</p>





<h3>Mindblindness theory</h3>

<p>The analysis of autism as "mind blindness"—the inability to create models of other people's thoughts.

The typical example of this is "where does X look for the object they stored, but which was moved by Y"—see theory of mind.

Not all autistics fit this pattern, however.</p>





<h3>Other Theories</h3>

<p>Other theories address the rise of autism in recent times. They suggests the rise of visual media and thereby

the increasing central role of visual information processing in the breakdown of language and the rise of autism.</p>

