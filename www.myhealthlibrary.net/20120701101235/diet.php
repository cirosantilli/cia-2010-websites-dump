<h2>:: Diet and Nutrition ::</h2>
<div class="rss-box">
<p class="rss-title"><a class="rss-title" href="http://www.sciencedaily.com/news/health_medicine/diet_and_weight_loss/" target="_blank">ScienceDaily: Diet and Weight Loss News</a></p>
<ul class="rss-items">
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120629211902.htm"  title="Cognitive skills such as learning and memory diminish with a..." target="_blank">Easter Island drug raises cognition throughout life span in mice</a> <br />
Cognitive skills such as learning and memory diminish with age in everyone, and the drop-off is steepest in Alzheimer's disease. Texas scientists seeking a way to prevent this decline reported exciting results this week with a drug that has Polynesian roots. The researchers added rapamycin to the diet of healthy mice throughout the rodents' life span. Rapamycin, a bacterial product first isolated from soil on Easter Island, enhanced learning and memory in young mice and improved these faculties in old mice, the study showed.
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120629005456.htm"  title="New research has demonstrated that people who ate more dieta..." target="_blank">What you eat can prevent arsenic overload</a> <br />
New research has demonstrated that people who ate more dietary vitamin B12 and animal protein had lower levels of arsenic (measured by deposition in toenails). Total dietary fat, animal fat, vegetable fat and saturated fat were also all associated with lower levels of arsenic, while omega 3 fatty acids, such as those found in fish oil, were associated with increased arsenic.
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120628174534.htm"  title="We've all heard that eating fish is good for our brains and ..." target="_blank">Why does a diet high in DHA improve memory?</a> <br />
We've all heard that eating fish is good for our brains and memory. But what is it about DHA, an omega-3 fatty acid found in fish, that makes our memory sharper?
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120628164207.htm"  title="Two nutrition experts say going gluten-free may be a good ch..." target="_blank">Going gluten-free: Is the diet a good fit for everyone?</a> <br />
Two nutrition experts say going gluten-free may be a good choice for some individuals, but that just because a product's label says it's gluten-free doesn't means that it's healthy.
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120628131358.htm"  title="Dietary fiber promotes a shift in the gut toward different t..." target="_blank">Dietary fiber alters gut bacteria, supports gastrointestinal health</a> <br />
Dietary fiber promotes a shift in the gut toward different types of beneficial bacteria, a new study shows.
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120628131245.htm"  title="Standing for long periods during pregnancy may curb the grow..." target="_blank">Standing for long periods during pregnancy may curb fetal growth: But working up to 36 weeks has no adverse effect, study suggests</a> <br />
Standing for long periods during pregnancy may curb the growth of the developing fetus, suggests new research.
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120627122254.htm"  title="Menopausal women who exercise may experience fewer hot flash..." target="_blank">Menopausal women could 'work out' their hot flashes</a> <br />
Menopausal women who exercise may experience fewer hot flashes in the 24 hours following physical activity, according to health researchers.
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120627103352.htm"  title="Children who help cook at home are more likely to enjoy frui..." target="_blank">Kids who cook are hungrier for healthy food choices</a> <br />
Children who help cook at home are more likely to enjoy fruits and veggies than kids who don't cook, according to a new study.
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120627092206.htm"  title="Women who regularly eat a low-carbohydrate, high-protein die..." target="_blank">Significant cardiovascular risk with low carbohydrate-high protein diets, experts warn</a> <br />
Women who regularly eat a low-carbohydrate, high-protein diet are at greater risk of cardiovascular disease (such as heart disease and stroke) than those who do not, a new study suggests.
</li>
<li class="rss-item"><a class="rss-item" href="http://www.sciencedaily.com/releases/2012/06/120626163807.htm"  title="In an examination of the effect on energy expenditure and co..." target="_blank">Effect of three common diets on energy expenditure following weight loss detailed</a> <br />
In an examination of the effect on energy expenditure and components of the metabolic syndrome of three types of commonly consumed diets following weight loss, decreases in resting energy expenditure and total energy expenditure were greatest with a low-fat diet, intermediate with a low-glycemic index diet, and least with a very low-carbohydrate diet, suggesting that a low-fat diet may increase the risk for weight regain compared to the other diets.
</li>
</ul></div>



