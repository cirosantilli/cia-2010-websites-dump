  <li><A href="http://www.fdanews.com/">FDAnews</A> - An electronic division of Washington Business Information Inc. with news on drugs, medical devices, and biologics.</li>
  <li><a href="http://www.globalrph.com" target="_blank">GlobalRPH </a>- Provides online calculators, intravenous dilution standards, and drug therapeutic categories.</li>
  <li><A href="http://www.goodspharmacy.com">Good's Pharmacy</A> - Specially schooled in the art of extemporaneous compounding, and can tailor drugs to a patient's special needs.</li>
  <li><A href="http://www.haiweb.org/">Health Action International</A> - A non-profit organization that represents the interests of consumers in drug policy and believes that all drugs marketed should be acceptably safe, effective, affordable and meet real medical needs.</li>
  <li><A href="http://www.howstuffworks.com/athletic-drug-test.htm">How Stuff Works: Athletic Drug Testing</A> - Unbiased explanations of all the various performance enhancing drugs, and how athletes are tested for them, with many related links.</li>
  <li><a href="http://www.infomed.org/100drugs" target="_blank">Infomed Drug Guide </a>- Intended for medical and pharmaceutical professionals and students.</li>
  <li><A href="http://www.anti-aging-drugs.com/">Life-Extension Corp.</A> - European business offering a variety of life-extension prescriptions. Categorized by illness, such as seizure or weight loss.</li>
  <li><a href="http://www.fda.gov/medwatch" target="_blank">MedWatch: The FDA Medical Products Reporting Program</a> - Purpose is to enhance the effectiveness of postmarketing surveillance of medical products as they are used in clinical practice and to rapidly identify significant health hazards associated with the products.</li>
  <li><A href="http://www.medicinenet.com/medications/focus.htm">MedicineNet: Medications and Over the Counter Drugs</A> - A complete list of commonly prescribed medications with a user friendly food and drug interaction tool.</li>
  	  <li><a href="http://www.mypillbox.org">My Pill Box </a>- Creates a printable schedule of your daily medications.</li>
	  <li><A href="http://www.orexpharma.com/">Orex Pharma Pvt., Ltd.</A> - Manufacturers and traders of bulk drugs, chemicals and pharmaceuticals in India. The company has large manufacturing facilities and it has inhouse Q.C and research and development and pilot plants. It can serve as a contract manufacturer.</li>
	  <li><a href="http://www.nursespdr.com" target="_blank">PDR Nurse's Drug Handbook </a><br>
  Includes additions, changes, and searchable drug database as a companion to the popular reference book.</li>
	  <li><a href="http://www.rxlist.com" target="_blank">RxList </a>- Drug name index with information for both the consumer and the medical professional.</li>
	  <li><A href="http://www.rxusainternational.com">RxUSA International, Inc.</A> - An international pharmacy serving physicians, hospitals, clinics and patients worldwide. We are distributors of USA-FDA approved medications, drugs, medical equipment and supplies, and accessories.</li>
	<li><a href="http://www.safemedication.com" target="_blank">Safe Medication.com</a> <br>
  Provides information about prescription medicines. From the American Society of Health-System Pharmacists.</li>
	<li><A href="http://www.erowid.org/">The Vaults of Erowid</A> &nbsp; - Index of drugs, with physical effects, images, history, chemistry and law information for each. Articles and sections for wide variety of drug related material, including law, experiences, community, drug testing, spirituality, health, and artistic endeavors.</li>
