<h2>:: Drug Lists ::</h2>
<ul>
 <li><a href="http://www.askapatient.com" target="_blank">Ask a Patient </a>- Features consumer ratings of prescription drugs, including comments on side effects, pharmaceutical rankings, reports, polls, and research help.</li>
