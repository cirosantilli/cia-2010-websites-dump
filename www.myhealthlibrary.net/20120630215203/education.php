  library.</li>
<li><a href="http://www.mybody.ie">My Body</a> - Interactive
  symptom checker, includes details of some possible
  causes.</li>
<li><a href="http://www.ncemi.org/cse/contents.htm">NCEMI:
  Common Simple Emergencies</a> - An online manual with detailed instructions and
  illustrations on how to treat various everyday medical emergencies.</li>
<li><a href="http://www.ohsuhealth.com/">OHSU Health</a> -
  Information for patients and clinicians on aspects of healthcare. Includes an index of health topics.</li>
<li><a href="http://www.polymedicine.com">PolyMedicine.com</a> -
  Clinicians review electronic medical information resources available in any of
  the following media: browsable WWW sites,
  downloadable datasets and software or CD-ROMS.</li>
<li><a href="http://www.predictonline.com">Predictonline</a> - A testing and awareness site to provide the public and healthcare provider
  with medical information on various medical conditions.</li>
<li><a href="http://preventdisease.com/">PreventDisease.com</a> - A patient-oriented portal for up-to-date health information on fitness,
  lifestyle, nutrition and health tools.</li>
<li><a href="http://members.tripod.com/docraza/">RJ's Medical Directory</a> - A collection
  of medical links and information categorized by specific topics.</li>
<li><a href="http://www.sahealthinfo.org">South African Health
  Information</a> - Reviewed, evidence-based health information based on the
  South African health system.</li>
<li><a href="http://www.talkmedical.com">Talk Medical</a> -
  Offers information on diseases, disorders, medications, parenting, and
  pregnancy. Also contains a searchable medical dictionary.</li>
<li><a href="http://www.themediweb.net">TheMediweb</a> - Provides a medical dictionary of health information for research and
  education for health care professionals, academics and the general public.</li>
<li><a href="http://www.vh.org">Virtual
  Hospital</a> - A digital health sciences library at
  the University of Iowa for health care providers and patients.</li>
<li><a href="http://www.vnh.org">Virtual Naval Hospital</a> - A
  digital library designed to help primary care providers and to help patients
  associated with naval medical issues. A service of the U.S.
  Navy Bureau of Medicine and Surgery.</li>
</ul>
