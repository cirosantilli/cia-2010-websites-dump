<li><a href="http://rad.usuhs.mil/medpix/medpix.html?mode=default#top">MedPix</a> - Database of medical images including
  radiology, pathology, and dermatology. Case of the Week with
  CME for physicians and CNE for nurses.</li>
<li><a href="http://www.merck.com/pubs/mmanual/">The Merck
  Manual</a> - Online version of The Merck Manual of Diagnosis and Therapy.</li>
