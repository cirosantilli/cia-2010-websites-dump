  required with free 4-week trial.</li>
<li><a href="http://www.carle.com/HealthGate/default.asp?id=PatientReports">Health
  Information Library</a> - Alphabetical listing of health information topics by
  the Carle Clinic and Hospital.</li>
<li><a href="http://www.healthsites.co.uk/">Healthsites UK</a> - A portal to medical and health information for patients and doctors. Access medical and NHS healthcare sites plus news, advice and
  complementary therapy resources.</li>
<li><a href="http://www.infoxchange.net.au/hhb/index.html">The
  Homeless Handbook</a> - An online manual designed to assist support workers of
  homeless and similarly disadvantaged people who find themselves in medically
  challenging situations.</li>
<li><a href="http://www.hospitalweb.co.uk/">HospitalWeb</a> - Directory of medical resources for professionals.</li>
<li><a href="http://www.medical-library.org/">Library of the
  National Medical Society</a> - Links to various journals, online diagnosis,
  medical textbooks and atlases. Subscription required.</li>
<li><a href="http://www.longevity.ca/">Longevity Healthy Aging
  Research Group</a> - Information from this Canadian organisation on getting older and staying healthy.</li>
<li><a href="http://www.mdconsult.com">MDconsult</a> - Includes reference books, journal search, practice guidelines, patient
  education and drug information. Subscription required with free 10-day pass.</li>
<li><a href="http://www.medcalc.com">MedCalc</a> - A comprehensive collection of online clinical calculators. Includes
  ABG, GFR, Fluids and BMI calculations and interpretations.</li>
<li><a href="http://www.medconnect.com/">MedConnect</a> - Information services with a clinical and practical focus for physicians and
  other health care professionals.</li>
<li><a href="http://www.medkb.com">Medical Knowledge Base</a> -
  Offers discussion forums on topics like cancer, AIDS, arthritis, hepatitis,
  healthy nutrition and motivation.</li>
</ul>
