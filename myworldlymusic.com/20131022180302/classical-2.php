<li><a href="http://en.wikipedia.org/wiki/Santoor" target="opennew">Santoor</a><br />
The santoor is an Persian stringed musical instrument which is trapezoid-shaped hammered dulcimer often made of walnut, with seventy strings. The special-shaped mallets (mezrab) are lightweight and are held between the index and middle fingers. A typical santoor has two sets of bridges, providing a range of three octaves.</li>
<li></li>
<li><a href="http://en.wikipedia.org/wiki/Violin" target="opennew">Violin</a><br />
The violin is a bowed string instrument with four strings usually tuned in perfect fifths. It is the smallest and highest-pitched member of the violin family of string instruments, which also includes the viola and cello.</li>
