<ul>
<li><a href="http://en.wikipedia.org/wiki/Sitar" target="opennew">Sitar</a><br />
The sitar is a plucked stringed instrument. It uses sympathetic strings along with a long hollow neck and a gourd resonating chamber to produce a very rich sound with complex harmonic resonance.</li>
<li></li>
<li><a href="http://en.wikipedia.org/wiki/Tabla" target="opennew">Tabla</a><br />
The tabla is a popular percussion instrument used in the classical, popular and religious music of the Indian subcontinent and in Hindustani classical music. The instrument consists of a pair of hand drums of contrasting sizes and timbres.</li>
<li></li>
<li><a href="http://en.wikipedia.org/wiki/Harmonium" target="opennew">Harmonium</a><br />
A harmonium is a free-standing musical keyboard instrument similar to a Reed Organ or Pipe Organ. It consists of free reeds and sound is produced by air being blown through reeds resulting in a sound similar to that of an accordion.</li>
<li></li>
