<h2>Country & Region-Specific Sources</h2><p>
	<div id="column2_text" class="scrollable">
	<a href="http://www.libraries.psu.edu/psul/researchguides/socialsciences/socscistats.html">National Statistical Agencies and Offices</a> - Penn State web site with an alphabetical country listing and a clickable map. <br>
    <a href="http://dir.yahoo.com/Government/Countries/">Yahoo! Government: Countries</a> - Alphabetical list of links to government web sites around the world. <br>
    <a href="http://publications.apec.org/">Asia-Pacific Economic Cooperation (APEC)</a> - Links to individual economy reports and economic indicators for its twenty-one member nations. <br>
    <a href="http://www.aseansec.org/13100.htm">Association of Southeast Asian Nations (ASEAN) Statistics</a> - Intra-ASEAN and extra-ASEAN statistics, communication and transportation statistics, and macroeconomic indicators. <br>
    <a href="http://www.caricomstats.org/">Caribbean Community (CARICOM)</a> - Trade statistics tables. <br>
    <a href="http://www.ecb.int/">European Central Bank (ECB)</a> - Data on the Euro, international reserves, retail interest rates, payment/cross border collateral statistics, historical time series, statistical press releases, and the ECB Monthly Bulletin. <br>
    <a href="http://europa.eu.int/en/comm/eurostat/serven/home.htm">EUROSTAT</a> - Statistical Office of the European Communities.<br>
    <a href="http://www.eclac.cl/estadisticas/default.asp?idioma=IN">ECLAC: Economic Commission for Latin America and the Caribbean</a> - Short-term indicators, foreign direct investment data, macroeconomic indicators, and other economic and social statistics (Note: Spanish language site.) <br>
    <a href="http://www.unescap.org/stat/index.asp">United Nations Economic and Social Commission for Asia and the Pacific (UNESCAP) Statistics Division</a> - Online publications include &quot;Asia and Pacific in Figures.&quot; <br>
       <a href="http://www.uneca.org/index.htm">United Nations Economic Commission for Africa</a> - Online publications include &quot;Economic and Social Conditions in Southern Africa&quot; and &quot;Economic Report on Africa.&quot; <br>
    <a href="http://www.unece.org/stats/stats_h.htm">United Nations Economic Commission for Europe (ECE) Statistics Online</a> - &quot;Trends in Europe and North America&quot; yearbook, &quot;Robotics Index,&quot; plus &quot;Gender Statistics,&quot; &quot;Human Settlement,&quot; and &quot;Gas Centre&quot; databases. </div>
