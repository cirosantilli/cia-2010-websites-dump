	  <h2 class="style1">&nbsp;</h2>
	  <p align="left"><strong class="style4">Investing -  Trading Abroad</strong><br />
	    <br />
	    By <a href="http://ezinearticles.com/?expert=Michael_Russell">Michael Russell</a>&nbsp;<br />
	    <br />
	    Want to trade shares  in New York in the day and Japan at night? In a fully  liberalized market, it's possible. If you have an Internet account, you can buy  stocks in major stock markets like Japan,  China, Australia, the US  and London.  Many other exchanges also have links to other markets that allow for orders to  be routed to the relevant market for execution and entered in the relevant  currency.<br />
	    If you're  well-traveled and have friends out of the country, they can probably introduce  you to a foreign broker. Otherwise, major local brokers have connections with  foreign brokers. The banks can TT (telegraph transfer) money to the assigned  brokers and do the conversion of funds for you. However, it's a burdensome  process unless your broker has a tied-up with a foreign house. Alternatively,  you could open an Internet trading account and $1000 is the minimum deposit to  start with. You have to go through a form, including declaring your foreign  status for tax purposes.<br />
	    The cost of  investing in shares is pretty much the same; you'd need to open an account and  pay stamp duties, have a trustee and account for transaction costs and  settlement fees. How much is worthwhile depends on how much you trade and what  the returns could be like. There is also a buy-sell spread in currency  conversion. And don't forget the cost of sending the money abroad; the costs of  transmitting funds abroad include cable charges from the remitting bank as well  as the receiving bank. There is no minimum amount required to open a foreign  currency account or to remit funds.<br />
	    The extra cost is  not a major issue as the important question is how much return can you get? You  need to look at the big picture when investing abroad and do the homework on the  country in which you want to invest.<br />
	    Investing directly  is for the sophisticated and educated investor. Investors can invest offshore  themselves but how well do you know the markets? It is a big pond out there.  There are thousands of companies listed, thus how do you ensure that you buy  the right stock? Familiarity is an important issue, where investors have access  to other markets like Australia;  they don't actively invest in Australia  securities because they don't understand the market. A single stock also may  not offer the diversification benefits of a large basket of stocks.<br />
	    One option for  international investing is exchange-traded funds (ETFs). They are like index  funds representing a basket of securities, but are traded like a stock. ETFs  have the ability to be traded throughout the day and have low expense ratios,  but some ETFs present liquidity issues. Unlike unit trusts where the manager  has the obligation to buy back from you at all times, small ETFs sometimes have  liquidity problems.<br />
	    There are two  important considerations before you start investing globally. First, be  educated; any investor should be an educated investor. For the global investor,  it is even more important to become connected to the global economy. Knowledge  is important even with advisers at hand.<br />
	    Do your homework,  read as much as you can on the markets, political and economic developments as  well as watch news channels and attend investment seminars. Travel and profit  from your holidays. First-hand knowledge of the countries is important. When  you travel, you see the actual cultures and economies.<br />
	    Second, know  yourself; in building portfolio, global investors need to examine their  objectives, risk profile and investment time horizon. For instance, a  conservative investor may not be able to take the volatility of a  Euro-denominated fund that fluctuates. Investors who cannot take currency risk  also shouldn't go abroad.<br />
	    Investors need to  understand that investing abroad is for medium to long term time horizon, for  this reason your money could be tied up for some time. Investors need a  longer-term view as the benefits of a global diversification show up over a  longer period and currency movements could cause volatility in the short term.  If your investment time horizon is short, you should avoid investing outside  your home market.<br />
	    Always get your feet  wet first. You don't have to wait until you have $1 million to start investing  abroad. You'll learn a lot, whether through gains or losses. If you have only a  few thousands dollars, invest in local unit trusts with overseas exposure.  Every few months, you'll get a report on how the fund manager invests and their  strategies. Learn from these reports.<br />
