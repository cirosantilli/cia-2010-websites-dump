	  <h2 class="style1">FINANCIAL E-ZINES </h2>
		<p align="left"><a href="http://www.activetradermag.com">Active Trader Magazine</a> is a monthly  financial magazine that features strategies, analysis and news for active  traders and investors. <br />
            <a href="http://www.tdmresearch.com/">A.I. Stock Forecast</a> is a  monthly online  stock and mutual fund report using artificial intelligence technologies.  Specific buy and sell recommendations provided. <br />
            <a href="http://www.appliederivatives.com">Appliederivatives.com</a> is a unique,  free, online magazine about all aspects of derivatives trading. <br />
            <a href="http://www.asiamoney.com/">AsiaMoney</a> is a subscription magazine that  features investment news, surveys, and equity management trends in Asia. Online users must pay a subscription fee for the  most recent articles. <br />
            <a href="http://www.abalert.com/">Asset-Backed Alert</a> offers a securitization weekly  covering asset-backed and mortgage-backed securities activities worldwide. <br />
            <a href="http://www.barrons.com/">Barrons</a> is a weekly publication for investors  from the publisher of the Wall Street Journal. Paid subscription is required  for access. Free trial is available. <br />
            <a href="http://blackwealthandfortunes.com">Black Wealth &amp; Fortunes Magazine</a> offers a personal finance publication for African Americans. Published quarterly and  includes retirement planning, college savings, buying mutual funds and  purchasing a home information. <br />
            <a href="http://www.commodities-now.com/">Commodities Now Online</a>  focuses on  trends in the commodities market, specifically, power and energy, precious  metals, and agriculture. <br />
            <a href="http://www.creditflux.com/">Creditflux</a> is a monthly journal covering the  market for credit derivatives, CDOs and structured credit. Site includes  articles from the print edition. <br />
            <a href="http://www.margrabe.com/">The Derivative Zine</a> offers articles about  equity and bond risk management, derivatives, and financial engineering. <br />
            <a href="http://dont-lost-money.info/">Don’t Lost Money</a> includes currency  and finance news and publications. <br />
            <a href="http://easy-money-investment.info/">Easy Money Investment Info</a> provides investing and property news and publications. <br />
            <a href="http://www.l2g.info/">EmergingMarketsNews.com</a> focuses on emerging  economies and features, news, interviews and reports to invest and travel in  emerging countries. <br />
            <a href="http://www.ftmandate.com/">Financial Times Mandate</a> is a weekly  international publication for asset management and custody news. <br />
            <a href="http://financialalert.co.nz/">Financialalert</a> provides investment,  insurance, business management and financial planning information and tools  service to help New Zealand  financial advisers. <br />
            <a href="http://www.forbes.com/">Forbes Magazine</a> gives information about companies  and the people who run them. Forbes success lists including the Platinum 400  best-performing U.S. big  companies, the Private 500 largest U.S. private firms, and the world's  richest people. <br />
            <a href="http://www.fdimagazine.com/">ForeignDirectInvestment</a> provides information  about foreign direct investment and the players who make it happen. Includes  regional surveys, interviews, multinational rankings, industry overviews,  tender wins and losses. <br />
            <a href="http://found-money-investment.info/">Found Money Investment</a> features forex, investing and money news and publications. <br />
            <a href="http://www.globalinvestormagazine.com/">Global Investor Magazine</a> is a  magazine dedicated to the international investment community. <br />
            <a href="http://www.growfolio.com/">growFolio</a> offers an  online investment publication  that features personal finance, business, and leisure information. <br />
            <a href="http://how-to-make-money-investment.info/">How to Make Money Investment</a> features investment news and advises. <br />
            <a href="http://www.institutionalinvestor.com/">Institutional Investor</a> offers financial news, features, and research. Daily coverage focuses on money  management, corporate finance, capital markets, alternative  investments,technology, and trading. <br />
            <a href="http://www.ifre.com/">International Financing Review</a> provides global capital markets intelligence. Also editorials on all aspects of capital  markets activity – debt, equities, derivatives, bonds, and loans. <br />
        <a href="http://investing-get-rich-quick.info/">Investing</a> offers business,  investments and property publications.<br />
        <a href="http://make-money-online-investment.info/">Make-Money-Online-Investment.info</a> features currency trading, money and investing news.<br />
          <a href="http://www.themoneyalert.com/">The Money Alert</a> features financial  articles, investment commentary, and retirement advice. <br />
            <a href="http://www.moneysense.ca/">MoneySense</a> is a Canadian magazine with a  focus on managing personal finances. <br />
            <a href="http://www.moneyweek.com">MoneyWeek Magazine</a> is a financial digest covering  UK  and international investing, and personal finance. <br />
            <a href="http://www.money-zine.com">Money-Zine.com</a> publication features  financial planning, career development and investing information. <br />
            <a href="http://www.oecd.org/finance/">OECD - Finance and Investment</a> -  features investment publications from OECD. Most of the publications focus on  the functioning of domestic and international investment markets. <br />
            <a href="http://www.offshoreinvestment.com/">Offshore Investment</a> is a professional  journal for international wealth managers including lawyers, accountants,  bankers and investors. Each issue contains feature articles and important  jurisdiction updates for the offshore financial industry. <br />
          <a href="http://www.tdameritrade.com">On-Line Investing Ideas</a> covers  currency trading, money, stocks and general investing topics. <br />
            <a href="http://portfolioconstruction.com.au">Portfolio Construction Forum</a> offers information and tools to help you build quality investor portfolios. <br />
            <a href="http://www.pwmnet.com/">Professional Wealth Management Magazine</a>  gives information for professional investment managers with news and analysis about  the products, market developments, tax issues and funds that impact the  portfolios of high net worth individuals. <br />
            <a href="http://theprospector.com">The Prospector</a> offers Mineral prospects and  mining claims of interest to investors and mine developers, in an e-magazine  format. <br />
            <a href="http://www.real-deals.co.uk">Real Deals</a> is a private equity and venture  capital magazine. <br />
            <a href="http://www.realert.com/">Real Estate Alert</a> is a  weekly newsletter on  investment opportunities in commercial real estate, commercial mortgages and  other real estate assets. <br />
            <a href="http://registeredrep.com">Registered Rep</a> is for retail investment  professionals, including stockbrokers, financial planners, branch managers and  the like, covering products such as stocks, bonds, derivatives / options,  commodities / futures, mutual funds, annuities, insurance and REITS. <br />
            <a href="http://www.securitiesindustry.com/">Securities Industry News</a>    for securities operations, technology and processing worldwide, covers global  custody, STP, T+1, ECNs, online trading, and correspondent clearing. <br />
            <a href="http://www.producersweb.com/home/index.php?SMC=1&amp;rIDX=299&amp;pageID=main">Senior  Market Advisor</a>  magazine offers guidance for advisors who sell annuities  and long term care insurance. <br />
            <a href="http://www.sfomag.com/">SFO Magazine</a> is an investment journal for  personal investing in stocks, futures and options. <br />
            <a href="http://www.sharesmagazine.com">Shares Magazine</a> is a UK magazine that aims to present UK market  information and share tips in a lively, accessible manner. <br />
            <a href="http://www.stocksandnews.com/">Stocks and News</a> is a unique and often  irreverent look at current events and their effect on the financial world. <br />
            <a href="http://www.telecomfinance.com/">TelecomFinance</a> offers news on debt and  equity financing of telecoms, satellite, internet and media projects globally. <br />
            <a href="http://www.ticker.com/">Ticker</a> is a financial magazine for investment  professionals. <br />
        <a href="http://www.tradersworld.com/">TradersWorld.com</a> offers an interactive  publication for stock and commodity traders.<br />
