<h1>Publications &amp; Online Magazines</h1>
<ul>
  <li><a href="http://www.boxoffice.com/" target="_films">Boxoffice Magazine</a> - Online version of the print magazine. Film reviews, news, industry classifieds, back issues, subscription information, and links.
  </li>
  <li><a href="http://www.cashiersducinemart.com" target="_films">Cashiers du Cinemart Online</a> - Online appendix to the infamous Detroit film zine.  Kooky news, interviews, and movie reviews.
  </li>
  <li><a href="http://www.cineaste.com/" target="_films">Cineaste Magazine</a> - Online version of the magazine focusing on the art and politics of cinema. Highlights of current and upcoming issues, subscription information, contributors, and links.
  </li>
  <li><a href="http://devon-cornwall-film.co.uk/" target="_films">D+CFilm</a> - Covering film and filmmaking-related news in Devon and Cornwall, UK. News, feature articles, resources, festival coverage, and community.
  </li>
  <li><a href="http://www.eclipsemagazine.com/" target="_films">Eclipse Magazine</a> - Reviews, polls, trailers and interviews.
  </li>
  <li><a href="http://efilmcritic.com/" target="_films">eFilmCritic.com</a> - Australia's movie magazine offering movie reviews by visitors to the site.
  </li>
  <li><a href="http://www.empireonline.com/" target="_films">Empire Online</a> - Online version of the print magazine provides news, reviews, feature articles, discussion groups, archives, search by title or text, and subscription details.
  </li>
  <li><a href="http://www.erratamag.com/" target="_films">Errata</a> - A collection of essays, reviews, commentary, and discussion about cinema.
  </li>
  <li><a href="http://www.eyeforfilm.co.uk/" target="_films">EyeForFilm</a> - UK-based online film magazine with news, reviews, feature articles, and festival coverage.
  </li>
  <li><a href="http://www.iol.ie/~galfilm/filmwest" target="_films">Film West Magazine</a> - Informative journal of Irish cinema including reviews, news, interviews from the world of film.
  </li>
  <li><a href="http://www.filmink.com.au/" target="_films">Filmink Magazine</a> - Film news and gossip, movie reviews, video and DVD releases, Australian and international film industry information.
  </li>
  <li><a href="http://www.filmsinreview.com/" target="_films">Films in Review</a> - Online version of the oldest film magazine in the United States. Background information, reviews, interviews, feature articles, as well as home entertainment reviews.
  </li>
  <li><a href="http://www.iofilm.co.uk/" target="_films">Inside Out Film</a> - Ezine with filmmaking resources, movie reviews, festival and local film news coverage, and links.
  </li>
  <li><a href="http://www.internetvideomag.com/" target="_films">Internet Video Magazine</a> - Tutorials, equipment descriptions, product reviews, featured internet films and videos.
  </li>
  <li><a href="http://www.zeldman.com/15/" target="_films">15 Minutes</a> - An interview magazine, featuring interviews with actors, writers, and directors from the world of film in RealVideo format.
  </li>
  <li><a href="http://www.shoestring.org/mmihome.html" target="_films">Movie Magazine International</a> - Weekly, nationally-syndicated radio show provides a look at the film scene, including movie reviews, weekly trivia contests, and interviews with actors and directors.
  </li>
  <li><a href="http://www.themovingarts.com/" target="_films">The Moving Arts Film Journal</a> - Features news, reviews, commentary, and a podcast.
  </li>
  <li><a href="http://www.movingpicturesmagazine.com/" target="_films">Moving Pictures Magazine</a> - Online version of the print magazine. Film, DVD, book, and soundtrack reviews, feature articles, interviews, multimedia, festival coverage, filmmaker profiles, filmmaking resources, and a section devoted to short film.
  </li>
  <li><a href="http://www.obsessedwithfilm.com/" target="_films">Obsessed With FIlm</a> - Reviews, feature articles, and discussion.
  </li>
  <li><a href="http://www.premiere.com/" target="_films">Premiere Magazine</a> - Online companion to the print magazine.
  </li>
  <li><a href="http://www.idmonsters.com/rtc/" target="_films">Right Turn Clyde</a> - An independent film zine with the ideals that Hollywood blockbusters tend to be sub par.
  </li>
  <li><a href="http://www.screendaily.com/" target="_films">Screen International</a> - Online version of the print magazine. Film industry news, movie reviews, feature articles, festival and awards coverage, production news, and special reports.
  </li>
  <li><a href="http://www.slantmagazine.com" target="_films">Slant Magazine</a> - Zine featuring reviews and articles about film and music.
  </li>
  <li><a href="http://www.totalfilm.com/" target="_films">Total Film</a> - Online version of the print magazine with news, weekly box office round-ups, release schedules for theaters, DVD, and Blu-ray, reviews, previews, trailers and clips, feature articles, contests, monthly free screenings in the UK, and forum.
  </li>
  <li><a href="http://www.trauma-magazine.com/" target="_films">Trauma Magazine</a> - European horror, science fiction, and fantasy film magazine.  Interviews, reviews, contests, screenplays, trivia, forum, and links.
  </li>
  <li><a href="http://www.videovista.net/" target="_films">VideoVista</a> - Film-related articles and interviews, reviews of video and DVD releases, and competitions.
  </li>
</ul>
