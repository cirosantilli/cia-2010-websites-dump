<h2>Background on Afghan Literature</h2>
<p class="source">Source: <a href="http://www.afghanistans.com/Information/People/Literature.htm" target="_blank">Afghanistans.com</a></p>
<p>The ancient art of storytelling continues to flourish in Afghanistan, partly in response to widespread
  illiteracy. This age-old practice of telling folktales, through music and the
  spoken word, is a highly developed and much appreciated art form. The use of folklore has become the thread that links the
  past with the present in Afghan society. Folktales concern all parts of Afghan
  life and often teach traditional values, beliefs, and behaviors. They are also
  a major form of entertainment in <st1:country-region>Afghanistan</st1:country-region>.</p>
<p>Literature in both the Dari and Pashto languages originated in the early Muslim centuries,
  when Arabic was also used. <i>Shah nameh</i> (Book of Kings), the great epic poem completed in 1010 by the Persian poet Firdawsi, consists of 60,000 rhyming couplets in Dari. Many
  other poems and tales were written in Dari and Turkic languages as well. Khushhal Kattak, a famous
  17th-century Pashtun warrior and poet, used verse to
  express the tribal code. Modern writings have attempted to bring Afghans closer
  to understanding the changes associated with the modern world, and especially
  to comprehend the destruction of their country by war. In 1972 Sayyed Burhanuddin Majruh wrote several volumes in classical, rhythmic Dari
  prose about a traveler who joins his countrymen in exile, where they exchange
  ideas and narratives from ancient times in the light of modern concepts of
  reason, logic, science, and psychoanalysis. During the war with the Soviets,
  writings focused on the twin concerns of Islam and freedom. Resistance to the
  Soviets was especially pronounced in the Pashto province of Paktia; in 1983 Gulzarak Zadran published &quot;
      <st1:country-region>Afghanistan</st1:country-region>
  the Land of Jihad: Paktiain Uprising Waves&quot; in the Pashto language. The Afghanistan Historical Society
  and the Pashto Academy published literary magazines and
  encouraged new writers in recent years, although much of their effort has been
  stopped by the most recent warfare. </p>

