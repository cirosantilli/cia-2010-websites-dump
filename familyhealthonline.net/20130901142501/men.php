Here's some exciting (if preliminary) news from a big study conducted by the University of Texas Southwestern Medical School, reported by Reuters: Prostate cancer patients who had been treated with either surgery or radiation, and who took aspirin or other anticoagulant drugs such as warfarin, were far less likely to die of cancer, the researchers said. Those who took the drugs had a 4 percent risk of dying from prostate cancer after 10 years, compared to 10 percent for men who did not take anticoagulants. Men with high-risk prostate cancer benefited the most, the researchers said ahead of an American Society for Radiation Oncology meeting, which starts next week in San Diego.  Choe's team looked at a study of 5,275 men whose cancer had not spread beyond the prostate gland. Of the men, 1,982 were taking anticoagulants. Those taking aspirin or other drugs to reduce clotting were far less likely to have the prostate tumors pop up elsewhere in their bodies and were less likely to die, Choe's team said in materials published ahead of the meeting. Read Full Post
